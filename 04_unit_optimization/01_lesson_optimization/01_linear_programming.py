"""
# 线性规划基础 (Linear Programming Basics)

## 1. 什么是线性规划？

在工程和商业领域，我们经常面临资源有限的情况。如何在有限的资源下，实现利润最大化或成本最小化？这就是**优化问题 (Optimization Problem)**。这一节，我们将学习其中最基础且应用最广的分支：**线性规划 (Linear Programming, LP)**。

线性规划是一种数学方法，用于在给定的线性约束条件下，寻找线性目标函数的最大值或最小值。

**线性规划问题的三个核心要素：**
1.  **决策变量 (Decision Variables)**：我们需要决定的量（例如：生产多少件产品 0，多少件产品 1）。为了与 Python 数组下标对齐，我们习惯用 $x_0, x_1, \dots, x_n$ 表示。一般这些变量取值是非负且**连续**的。
2.  **目标函数 (Objective Function)**：我们希望最大化或最小化的目标（例如：总利润、总成本）。它必须是决策变量的**线性函数**。
    *   例如：$\max Z = c_0x_0 + c_1x_1 + \dots + c_nx_n$
3.  **约束条件 (Constraints)**：限制决策变量取值的条件（例如：原材料的限制、工时的限制）。它们必须是决策变量的**线性等式或线性不等式**。
    *   例如：$a_{00}x_0 + a_{01}x_1 \le b_0$

**满足以上三个条件**的数学模型称为**线性规划**的数学模型。

**为什么叫“线性”？**
因为目标函数和所有的约束条件中，变量的最高次数都是 1，没有平方、开方、对数或变量相乘的情况。在几何上，这意味着约束条件在二维空间中表现为直线，在三维空间中表现为平面。

"""

