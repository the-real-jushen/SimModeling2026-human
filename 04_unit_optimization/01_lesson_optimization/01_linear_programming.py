"""
## 2. 实例：工厂生产计划与 SciPy 求解

**问题描述：**
一家工厂生产两种产品：产品 0 和产品 1。
*   生产一件产品 0 需要 1 小时工时和 4 单位原材料，利润为 2 万元。
*   生产一件产品 1 需要 2 小时工时和 0 单位原材料，利润为 3 万元。
*   工厂每天最多有 8 小时工时可用。
*   工厂每天最多有 16 单位原材料可用。
*   另外，由于市场需求限制，产品 1 每天最多只能生产 3 件。

**问：工厂每天应该生产多少件产品 0 和产品 1，才能使总利润最大？**

### 2.1 建立数学模型

1.  **决策变量：**
    *   设 $x_0$ 为每天生产产品 0 的数量。
    *   设 $x_1$ 为每天生产产品 1 的数量。
    *   隐含约束（边界 Bounds）：产量必须非负且产品 1 的销量受限，即 $x_0 \ge 0, 0 \le x_1 \le 3$。

2.  **目标函数（最大化利润）：**
    *   $\max Z = 2x_0 + 3x_1$

3.  **不等式约束条件 (Constraints)：**
    *   工时限制：$1x_0 + 2x_1 \le 8$
    *   原料限制：$4x_0 + 0x_1 \le 16$
    
> **优化小贴士**：对于 $x_1 \le 3$ 这样的单变量上下限，我们没必要把它塞进约束条件系数矩阵（A_ub）中，过度膨胀的矩阵会导致解算器效率低下。应当直接利用算法底层的边界配置（Bounds）来实施限制。

### 2.2 转化为 SciPy 标准形式

Python 的 `scipy.optimize.linprog` 函数默认求解的是**最小化 (Minimization)** 问题，并且所有的不等式约束都必须是 **小于等于 ($\le$)** 的形式。

因此，我们需要对模型进行转换：
*   **最大化转最小化：** 求 $\max Z$ 等价于求 $\min (-Z)$。所以目标函数要乘以 $-1$，变为 $\min (-2x_0 - 3x_1)$。
*   **约束条件：** 已经是 $\le$ 形式，无需转换。

"""

# %%
import numpy as np
from scipy.optimize import linprog

# 1. 定义目标函数的系数 (注意：求最大化需要将系数取负)
# 目标函数: min -2*x0 - 3*x1
c = [-2, -3]

# 2. 定义不等式约束的系数矩阵 (A_ub) 和右侧常数向量 (b_ub)
A_ub = [
    [1, 2],  # 工时: 1*x0 + 2*x1 <= 8
    [4, 0]   # 原料: 4*x0 + 0*x1 <= 16
]
b_ub = [8, 16]

# 3. 定义决策变量的边界 (Bounds)
# 我们在这里直接约束 x1 的上限为 3，无需写进上方矩阵
x0_bounds = (0, None) # x0 >= 0 (正无穷)
x1_bounds = (0, 3)    # 0 <= x1 <= 3
bounds = [x0_bounds, x1_bounds]

# 4. 调用 linprog 求解
# method='highs' 是目前推荐的高性能求解器
result = linprog(c, A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')

# 5. 详细输出和解释 linprog 的运行结果属性
print("--- 线性规划求解结果 ---")
if result.success:
    print(f"求解成功: {result.success}")
    # result.x 包含了最优的决策变量值
    print(f"最优生产计划：生产产品 0 约 {result.x[0]:.2f} 件，生产产品 1 约 {result.x[1]:.2f} 件")
    # result.fun 是目标函数的最小值，因为我们之前取了负号，现在要再取负号还原回去
    print(f"最大总利润：{-result.fun:.2f} 万元")
    
    # 获取其他系统状态与解释
    print("\n[更多求解器细节 (result属性)]")
    # .nit：求解过程用到的迭代循环次数
    print(f"迭代次数 (.nit): {result.nit}")
    # .status：退出状态代码，0代表成功。1代表超出迭代，2代表无解，3代表无界解等。
    print(f"状态码 (.status): {result.status}")
    # .slack：与不等式约束条件隔着上限门槛之间的差距。例如 0 表示这个维度的资源已经被彻底榨干用完了。
    print(f"松弛余量 (.slack): {result.slack}") 
else:
    # .message：求解器失败时，返回的无法找到最优配置的原因说明
    print("求解失败：", result.message)




