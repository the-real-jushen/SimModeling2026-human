"""
# 非线性规划与梯度下降 (Non-linear Programming)

在前面的课程中，我们学习了线性规划（LP）。然而，现实世界中的许多物理和经济规律并不是线性的。
例如，发电厂的燃料成本与发电量之间通常是“二次函数”关系；空气阻力与速度的平方成正比。

## 1. 遇到非线性问题的降维打击：近似线性化 (Linearization)

在算力匮乏或模型极度庞大的时代，工程师们在遇到非线性目标（如存在平方项的二次成本曲线）时，最常见的降维妥协手法便是**线性化 (Linearization)**。
即采用分段折线或割线的方法来逼近原来的曲线方程。

**案例参考：发电机组经济配电**
假设有两个发电机组 $G_1$ 和 $G_2$。我们需要满足总负荷需求为 100 MW（即 $P_{G1} + P_{G2} = 100$）。
但它们的燃料成本都是出力的二次函数：
*   机组 1 成本：$C_1(P_{G1}) = 0.0007 P_{G1}^2 + 0.30 P_{G1} + 4$ ，且出力受限在 $30 \le P_{G1} \le 150$ 之间。
*   机组 2 成本：$C_2(P_{G2}) = 0.0004 P_{G2}^2 + 0.32 P_{G2} + 3$ ，且出力受限在 $0 \le P_{G2} \le 50$ 之间。

**初级线性化操作：两端割线法**
最简单的方法是只取曲线首末两端（起点和终点）的极值差，连一条直线代替抛物线。例如机组1通过代入 $P_{G1}=30$ 和 $150$ 两点，可算出近似的一次直线。但首尾相连的方式跨度太大，中间隆起形成的**误差极大**！

**高级解法进阶：分段线性化 (Piecewise Linearization)**
为了降低误差，我们可以把机组的发电区间“切碎”成多段！只要目标是求极小值且它是“凸函数（开口向上，边际成本递增）”，我们甚至都不需要引入复杂的整数状态控制变量，可以直接用上一节学的**标准 LP (PuLP)** 极速求解。

例如，我们将 $G_1(30 \to 150)$ 的区间均分为两段：`30-90` ($P_{11}$)、`90-150` ($P_{12}$)，经过计算其基础二次多项式差值可以得出，前一段斜率(即度电单价约为0.384)会低于后一段(0.468)；
同理，将 $G_2(0 \to 50)$ 也均分两段：`0-25` ($P_{21}$)、`25-50` ($P_{22}$)。
下面我们通过 PuLP 写出这段非常精妙的数学降维替换代码，它用多段“阶梯电价”一样的直线段完美逼近了大抛物线：

"""

# %%
import pulp

# 1. 建立问题：求最小化成本
prob = pulp.LpProblem("Piecewise_Linearization", pulp.LpMinimize)

# 2. 定义分段变量！把一台机组的出力拆出分段，分别代表它在对应区间的“增量发段”
# G1 基础部分从30起步。 x11代表 30-90 的增量发量， x12代表 90-150 的增量发量
x11 = pulp.LpVariable('PG1_seg1', lowBound=0, upBound=60, cat='Continuous') # 容量上限 60
x12 = pulp.LpVariable('PG1_seg2', lowBound=0, upBound=60, cat='Continuous') # 容量上限 60

# G2 基础部分从0起步。 x21代表 0-25 的增量发量， x22代表 25-50 的增量发量
x21 = pulp.LpVariable('PG2_seg1', lowBound=0, upBound=25, cat='Continuous') # 容量上限 25
x22 = pulp.LpVariable('PG2_seg2', lowBound=0, upBound=25, cat='Continuous') # 容量上限 25

# *非常重点的核心学问解释：
# 因为抛物线开口向上，越往后段成本攀升越快（斜率递增）。在寻找【最小化】结果的倒逼驱动下，
# 虽然完全没有给 x11 和 x12 写先后顺序的硬性逻辑约束，但求解器天然会“贪小便宜”，它绝对会
# 优先把单价便宜的 “seg1” 用满榨干，之后才去无奈地买单价贵的 “seg2”！这构成了完美的平滑过渡。

# 3. 目标函数：各段基础起跑线成本绝对值 + (第一阶梯价*量 + 第二阶梯价*量)
cost_G1 = (0.0007*(30**2)+0.3*30+4) + 0.384 * x11 + 0.468 * x12  
cost_G2 = (3.00)                    + 0.330 * x21 + 0.350 * x22  
prob += cost_G1 + cost_G2

# 4. 负荷约束：总出力 = 基础起步值 + 所有分段增量包的总和 = 100MW
PG1_Total = 30 + x11 + x12
PG2_Total = 0  + x21 + x22
prob += (PG1_Total + PG2_Total == 100, "Total_Demand_100MW")

# 5. 求解
prob.solve(pulp.PULP_CBC_CMD(msg=0))

print("---近似分段线性化方案结果---")
pg1_opt = 30 + x11.varValue + x12.varValue
pg2_opt = 0 + x21.varValue + x22.varValue
print(f"建议出力：机组1={pg1_opt:.2f} MW，机组2={pg2_opt:.2f} MW")
print(f"预测总耗费：{pulp.value(prob.objective):.2f} 元/小时")




