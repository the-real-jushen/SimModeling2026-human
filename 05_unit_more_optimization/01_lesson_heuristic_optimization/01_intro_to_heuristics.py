"""
## 维度灾难 (The Curse of Dimensionality) 与有限算力

既然只能通过“试一试”寻找解，我们能不能把所有可能的情况都试遍，从中挑出一个最好的？

* 如果问题非常简单，搜索空间极小，我们完全可以**穷举（暴力搜索）**所有可行解。
* 但实际问题往往涉及非常多个变量，随着问题维度的增加，即使每个变量的可选范围很小，所有解的组合数量也会呈指数级爆炸，这就是**维度灾难 (Curse of Dimensionality)**。

对于一些看似十分简单的组合优化或排样问题，它们的解的数量往往是一个天文数字。要想把所有可能解硬算一遍，**即使用世界上最快的超级计算机，在宇宙毁灭之前也不可能算完**。

因此，我们不仅不能把所有情况试一遍，我们往往只能触及到巨大解空间冰山一角的极小部分。搜索的宗旨变成了：**不仅要找，还要聪明地找，只要能在合理的时间内找到一个“足够好”的解就可以了。**

"""

