# %%
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import statsmodels.api as sm

# 1. 准备实测原始数据：每周广告费(x)与它拉动的销售额(y)
x_ad = [63566, 50762, 50941, 17597, 33029, 58543, 60492, 59686, 16432, 17262, 39118, 36078, 42113, 50562, 38240, 59870]
y_sales = [651334, 527670, 523751, 175467, 377978, 520100, 620856, 593739, 181949, 184644, 379374, 238688, 410066, 413541, 340242, 582843]

# 构建成 DataFrame 方便查阅展示
df = pd.DataFrame({'Ad_Expense': x_ad, 'Sales': y_sales})

X = df['Ad_Expense']
y = df['Sales']

# 手动为自变量 X 增加一列常数截距全 '1' 矩阵
X_int = sm.add_constant(X)

# 2. 建立并训练模型
# 请特殊注意参数顺序：先 y，再 X_int
model = sm.OLS(y, X_int) 
res = model.fit()

# 使用 summary() 一键打印这巨细无靡的模型全景报告！
print(res.summary())




