# %% [markdown]
"""
循环是处理重复任务的核心。在使用 `for` 循环时，Python 提供了一个非常好用的 `range()` 函数来生成一个 Iterable（可迭代数字序列）。
- `range(stop)`：生成 0 到 stop-1 的序列（共 stop 个数）。
- `range(start, stop, step)`：还可以控制起点、终点和步长。

"""

# %%
# 基础 for 循环
print("--- range(5) 会生成 0, 1, 2, 3, 4 ---")
for i in range(5):  
    print(i, end=" ")
print("\n")

# 带步长的 range 系列
print("--- range(1, 10, 3) 从 1 开始，步长为 3 ---")
for i in range(1, 10, 3):
    print(i, end=" ")
print("\n")

# 也可以倒着数
print("--- range(3, -2, -2) 步长为负数 ---")
for i in range(3, -2, -2):
    print(i, end=" ")
print("\n")

# While 循环
print("--- While 倒计时 ---")
n = 3
while n > 0:
    print(n, end=" ")
    n = n - 1
print()




# %% [markdown]
"""
**循环控制 (Break / Continue)**
与其它语言一样，你可以使用 `break` 提前终止循环的执行，或使用 `continue` 跳过当前这一轮剩余代码，直接进入下一次迭代。

"""

# %%
print("--- Break 与 Continue 测试 ---")
for i in range(10):
    if i % 2 == 0:
        # 当遇到偶数时，跳过下方所有的代码，直接进入下个数字
        continue 
    
    if i > 6:
        # 当数字大于 6，直接彻底把整个大循环掐断
        break    
        
    print(i) 
# 想一想：程序最终会打印出什么结果？答案是：1, 3, 5




# %% [markdown]
"""
## 3. 函数包装：不要重复造轮子

当你有很多需要被重复利用的代码（比如计算某条物理定律的轨迹），你可以把它定义为一个函数，并在之后的代码中通过参数列表给它传值。

使用 `def` 关键字来定义一个函数。

"""

# %%
# 定义一个计算自由落体距离的函数
# 这里的 g 和 t 是输入参数列表
def freeFall(g, t):
    # 下落距离公式：H = 0.5 * g * t^2
    h = 0.5 * g * (t ** 2)
    return h

# 调用刚才写好的函数，并给参数传值
height = freeFall(9.8, 5)
print(f"重力加速度为9.8时，下落 5 秒的距离是: {height} 米")




