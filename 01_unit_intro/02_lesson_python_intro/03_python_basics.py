# %% [markdown]
"""
列表的**索引 (Indexing)** 与 **切片 (Slicing)**：
1. 你可以正向 index 它（从 0 开始），也可以借由负数逆向回数（`-1` 代表倒数第一个元素）。
2. 你还可以通过 `[start:end]` 对它进行区间斩断。其核心规则是“**包头不包尾**”。
3. 或者利用步长 `[start:end:step]` 实现了跳跃获取。

"""

# %%
test_arr = [0, 1, 2, 3, 4, 5]
print("正向索引位置 2 处的值:", test_arr[2])
print("切片下标倒数一号元素:", test_arr[-1])

# 区间斩断操作 [start:end)
print("提取前两个元素:", test_arr[0:2])
print("从下标 2 开始获取到最后的所有:", test_arr[2:])

# 还可以搭配步长功能跨点获取
print("从头切到第 5 个，步长设为 2:", test_arr[0:5:2])

# 支持连体加法 (拼接)
b_arr = test_arr[:3] + [101, 102] + test_arr[-2:]
print("强行合并插入结果:", b_arr)




