"""
## 2. 定义参数和变量 (Define Parameters and Variables)

根据第一步确定的目标，第二步就是要把所有会影响这个目标的物理量或因素，全部抽象成数学符号。在这个过程中，你必须根据工程经验做出明智的**假设 (Assumptions)** 和 **简化 (Simplifications)**。

继续过山车的例子：
- **剥离无用信息**：如果你决定用“最大加速度”来定义刺激，那么座椅的颜色、背景音乐的音量、甚至轨道的材质（如果你假设钢轨和车轮的摩擦力远小于重力势能，可以直接忽略它带来的动能损耗），这些信息都可以被无情抛弃。
- **保留核心要素**：必须被赋予数学符号的是那些决定生死的量：过山车的初始速度设为 $v_0$、第一个下降坡的高度差设为 $h$、轨道的曲率半径设为 $R$。

（我们将在下一节单独且深入地探讨参数与变量的区别，这也是工程数学中的一个核心概念。）

## 3. 建模与求解 (Model and Solve)

当你把现实世界的问题全部变成了纸上的符号（目标、已知量、未知量）之后，就是时候打开你的“数学工具箱”了。你要根据问题的性质，挑选出合适的数学工具或算法来连接这些符号。

- 如果你要计算过山车在最低点的极速和向心加速度，你需要选用经典的物理学工具：**牛顿运动定律**和**动能定理积分**。
- 如果你要分析游客排队的拥堵情况以最大化利润，由于到达人数是随机的，你需要选用**概率论与排队论**模型。
- 如果系统极其复杂，包含大量约束条件（比如预算不能超过 $5000万，占地不能超过 $2000平米），你可能需要用到这门课后续会教的**运筹学与启发式优化算法**（如遗传算法）。

在工程师的手中，这个所谓的“求解”过程，早就不用纸笔和草稿纸手算了，而是**写成 Python 代码，让计算机替我们算出结果**。

"""

