"""
## 4. 评价与迭代模型 (Evaluate the Model)

算出了数字，任务就结束了吗？绝对没有！回到我们在第一节课学到的铁律：“所有模型都是错的”。
由于我们在第二步为了方便计算，引入了大量的简化和假设，所以模型算出的结果，极有可能在现实中是一个彻头彻尾的灾难。

因此，最后一步我们必须对模型进行严厉的**评价和合理性检验**（Sanity Check）。

**不合理的极限结果**
比如，在做过山车理论规划时，你的 Python 程序告诉你：为了达到让游客失重 10 秒钟的最大刺激效果，过山车需要以 300km/h 的速度进入一个半径仅为 5 米的环形轨道。
这个数学解可能是极其准确的。但是，如果你把它直接交给施工队，恭喜你，你将制造一场命案。因为在这个速度和曲率下，游客承受的 G 力会直接导致他们昏厥甚至更严重的结果。

这说明什么？说明你在一开始建立模型的时候，**漏掉了一个致命的约束条件**：人体的生理极限（如向心力必须在 $5G$ 以内）。

**模型的闭环迭代**
当你发现结果不合理时，你不需要完全推翻重来。建模是一个迭代的闭环：
发现问题 -> 返回步骤1或2（加入最大向心力的数学约束条件） -> 重新运行代码求解 -> 再次验证 -> 直到找到一个在理论上刺激，在生理上安全的完美方案。

这就是真正的工程闭环思维：**定义系统 -> 抽象表示 -> 求解算法 -> 实际验证 -> 修正迭代**。

我们在后续的每一个 Python 实战项目中，都会严格遵循这个流程。

"""

